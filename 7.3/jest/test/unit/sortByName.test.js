const sorting = require('../../app.js');

describe('Books names test suit', () => {
  it('Books names should be sorted in ascending order', () => {
    expect(
      sorting.sortByName([
        'Гарри Поттер',
        'Властелин Колец',
        'Волшебник Изумрудного города',
      ])
    ).toEqual([
      'Властелин Колец',
      'Волшебник Изумрудного города',
      'Гарри Поттер',
    ]);
  });

  it('сортирует массив книг в обратном порядке по первым буквам', () => {
    expect(
      sorting.sortByName([
        'Ярмарка тщеславия',
        'Король Лир',
        'Анна Каренина',
      ])
    ).toEqual([
      'Анна Каренина',
      'Король Лир',
      'Ярмарка тщеславия',
    ]);
  });

  it('не меняет порядок одинаковых названий', () => {
    expect(
      sorting.sortByName([
        'Война и мир',
        'Анна Каренина',
        'Война и мир',
      ])
    ).toEqual([
      'Анна Каренина',
      'Война и мир',
      'Война и мир',
    ]);
  });

  it('сортирует без учёта регистра для книг', () => {
    expect(
      sorting.sortByName([
        'Улисс',
        'три мушкетера',
        'Дон Кихот',
      ])
    ).toEqual([
      'Дон Кихот',
      'три мушкетера',
      'Улисс',
    ]);
  });

  it('оставляет уже отсортированный массив без изменений', () => {
    const input = [
      'Анна Каренина',
      'Властелин Колец',
      'Гарри Поттер',
    ];
    expect(sorting.sortByName(input)).toEqual([
      'Анна Каренина',
      'Властелин Колец',
      'Гарри Поттер',
    ]);
  });
});
